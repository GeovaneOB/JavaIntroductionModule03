package org.example;

public class LacoDeRepeticao {

    public static void main(String[] args) {

        System.out.println("\n====== LAÇO DE REPETIÇÃO DE 100 ATÉ 120 - SOMENTE N°s PARES ======\n");

        for (int i = 100; i <= 120; i+=2) {
            System.out.println(i);
        }

    }

}
